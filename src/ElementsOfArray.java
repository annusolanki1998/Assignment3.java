public class ElementsOfArray {
    public static void main(String[] args) {
        int[] array = {-8,-9,0,1,2,3,5,9,4};
        System.out.println("Elements of given array are : ");

        for(int i=0; i<=array.length-1; i++)
        {
            System.out.print(array[i] + " ");
        }
    }
}
